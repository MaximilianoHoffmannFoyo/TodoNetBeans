package act11;

/**
 *
 * @author maxi
 */
public abstract class Figura {
    
    private int base;
    private int altura;

    public Figura() {
    }

    public Figura(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
    public abstract int calcularArea();
}
